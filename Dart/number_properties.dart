//Number properties

void main(){
  
  var num = 10;

  print("Number properties \n Number = $num \n");

  print("Hashcode: ${num.hashCode}");
  print("isFinite: ${num.isFinite}");
  print("isInFinite: ${num.isInfinite}");
  print("isNan: ${num.isNaN}");
  print("isNegative: ${num.isNegative}");
  print("Sign: ${num.sign}");
  print("isEven: ${num.isEven}");
  print("isOdd: ${num.isOdd}");
  print("bitlength: ${num.bitLength}");

}